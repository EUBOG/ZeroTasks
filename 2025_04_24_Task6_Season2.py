# Задача: написать функцию season, принимающую 1 аргумент — номер
#  месяца (от 1 до 12), и возвращающую время года, которому этот
#  месяц принадлежит (зима, весна, лето или осень).

# Вариант 2. Используем словарь.

def season(month):
    seasons = {
        (12, 1, 2): "зима",
        (3, 4, 5): "весна",
        (6, 7, 8): "лето",
        (9, 10, 11): "осень"
    }
    for months, season_name in seasons.items():
        if month in months:
            return season_name
    return "Неверный номер месяца. Введите число от 1 до 12."

# Примеры использования
print(season(1))   # зима
print(season(4))   # весна
print(season(7))   # лето
print(season(10))  # осень
print(season(13))  # Неверный номер месяца. Введите число от 1 до 12.