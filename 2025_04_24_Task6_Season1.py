# Задача: написать функцию season, принимающую 1 аргумент — номер
#  месяца (от 1 до 12), и возвращающую время года, которому этот
#  месяц принадлежит (зима, весна, лето или осень).

# Вариант 1. Используем условные операторы if-elif-else для проверки принадлежности месяца к сезону.
# Проверяем группы месяцев с помощью кортежей: (12, 1, 2) - зима и т.д.

def season(month):
    if month in (12, 1, 2):
        return "зима"
    elif month in (3, 4, 5):
        return "весна"
    elif month in (6, 7, 8):
        return "лето"
    elif month in (9, 10, 11):
        return "осень"
    else:
        return "Неверный номер месяца. Введите число от 1 до 12."

# Примеры использования
print(season(1))   # зима
print(season(4))   # весна
print(season(7))   # лето
print(season(10))  # осень
print(season(13))  # Неверный номер месяца. Введите число от 1 до 12.